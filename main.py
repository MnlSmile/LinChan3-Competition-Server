@app.post('/pillars_of_fortune/require_close')
async def require_close(metadata:POFMetadata, req:Request) -> dict:
    if req.client.host in ['127.0.0.1']:
        procs = get_processes_by_port(req.client.port)
        if len(procs) > 0:
            for proc in procs:
                if proc['name'] == 'bedrock_server.exe' or 'bedrock_server.exe' in proc['exe_path']:
                    try:
                        subprocess.run(f'taskkill /F /PID {proc['pid']}', shell=True, check=True)
                        print(f"已终止进程 PID: {proc['pid']}")
                    except subprocess.CalledProcessError as e:
                        print(f"终止进程失败: {e}")
                        traceback.print_exception(e)
        return {'status': 'ok'}
    else:
        return {'status': 'failed'}

@app.post('/pillars_of_fortune/upload_result')
async def upload_result(metadata:POFMetadata) -> dict:
    db = await pool.acquire()
    async with db.cursor() as cur:
        pof = pypika.Table('pillars_of_fortune')
        query = pypika.MySQLQuery.update(pof) \
            .set(pof.creationTime, metadata.creationTime) \
            .set(pof.version, metadata.version) \
            .set(pof.minecraftVersion, metadata.minecraftVersion) \
            .set(pof.playersCount, metadata.playersCount) \
            .set(pof.playerList, json.dumps(metadata.playerList)) \
            .set(pof.game, metadata.game) \
            .set(pof.LCCompetitionServerVersion, metadata.LCCompetitionServerVersion) \
            .set(pof.replayFilePath, metadata.replayFilePath) \
            .set(pof.replayRemotePath, metadata.replayRemotePath) \
            .set(pof.started, int(metadata.started)) \
            .set(pof.canceled, int(metadata.canceled)) \
            .set(pof.finished, int(metadata.finished)) \
            .set(pof.startReason, metadata.startReason) \
            .set(pof.cancelReason, metadata.cancelReason) \
            .set(pof.finishReason, metadata.finishReason) \
            .set(pof.startTime, metadata.startTime) \
            .set(pof.finishTime, metadata.finishTime) \
            .set(pof.ranking, json.dumps(metadata.ranking)) \
            .set(pof.data, json.dumps(metadata.data)) \
            .where(pof.id == metadata.id)
        try:
            await cur.execute(str(query))
            await db.commit()
        except Exception as e:
            traceback.print_exception(e)
            await db.rollback()
    return {}

