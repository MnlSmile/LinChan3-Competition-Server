def get_processes_by_port(port):
    result = []
    
    for conn in psutil.net_connections():
        if hasattr(conn.laddr, 'port') and conn.laddr.port == port:
            try:
                process = psutil.Process(conn.pid)
                process_info = {
                    'pid': process.pid,
                    'name': process.name(),
                    'status': conn.status,
                    'local_address': f"{conn.laddr.ip}:{conn.laddr.port}",
                    'remote_address': f"{conn.raddr.ip}:{conn.raddr.port}" if conn.raddr else None,
                    'create_time': process.create_time(),
                    'exe_path': process.exe(),
                    'cmdline': ' '.join(process.cmdline())
                }
                result.append(process_info)
            except (psutil.NoSuchProcess, psutil.AccessDenied):
                continue
    
    return result

@app.post('/pillars_of_fortune/download_metadata')
async def download_metadata(metadata:POFMetadata) -> POFMetadata:
    global test_uuid_id_map
    
    conn = await pool.acquire()
    try:
        async with conn.cursor() as cur:
            pof = pypika.Table('pillars_of_fortune')
            max_retries = 10
            retry_count = 0
            
            while retry_count < max_retries:
                query = pypika.MySQLQuery.from_(pof).select(pof.uuid).where(pof.uuid == metadata.uuid)
                await cur.execute(str(query))
                result = await cur.fetchall()
                
                if len(result) > 0:
                    metadata.uuid = str(uuid.uuid4())
                    retry_count += 1
                else:

                    break
            
            if retry_count >= max_retries:
                timestamp = int(time.time() * 1000)
                metadata.uuid = f"gen-{timestamp}"
            
            metadata.creationTime = int(time.time() * 1000)
            metadata.LCCompetitionServerVersion = '0.9.0'

            query = pypika.MySQLQuery.into(pof).columns(
                'uuid', 'creationTime', 'version', 'LCCompetitionServerVersion'
            ).insert(
                metadata.uuid, 
                metadata.creationTime, 
                metadata.version, 
                metadata.LCCompetitionServerVersion
            )
            
            await cur.execute(str(query))
            await conn.commit()

            await cur.execute("SELECT LAST_INSERT_ID()")
            result = await cur.fetchone()
            if result and result[0]:
                metadata.id = result[0]

            test_uuid_id_map[metadata.uuid] = metadata.id
            
    except Exception as e:
        traceback.print_exc()
        if conn:
            await conn.rollback()
    finally:
        if conn:
            pool.pool.release(conn)
    
    return metadata

